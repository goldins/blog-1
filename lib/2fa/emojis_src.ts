const r = new RegExp(/([-_][0-9a-f]{2,5}){1,7}.png$/);

export const emojis = ems.map((em) => {
  // find hex values
  const matches = em.match(r);
  if (!matches || !matches[0]) {
    throw new Error('No match');
  }
  // after splitting by space, ignore first item (empty string) that was produced when
  // replacing `-_` with ` 0x`. See below.
  // e.g. ' 0x11 0x12 0x13' => [' ', 0x11, 0x12, 0x13]
  const [, ...emoji] = matches[0]
    .replace('.png', '')
    // remove (redundant?) fitzpatrick modifiers.
    .replace(
      /(1f3fb)_(1f3fb)|(1f3fc)_(1f3fc)|(1f3fd)_(1f3fd)|(1f3fe)_(1f3fe)|(1f3ff)_(1f3ff)/g,
      '$1$3$5$7$9'
    )
    // replace dividers with 0x.
    .replace(/[-_]/g, ' 0x')
    // this will introduce an extra space at the start of the string:
    // e.g. '_1f_20' => ' 0x1f 0x20'
    // which will be droppped later.
    .split(' ');
  // console.log(emoji);
  return String.fromCodePoint(...emoji.map((v) => +v));
});

